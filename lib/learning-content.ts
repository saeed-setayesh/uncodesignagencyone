/** عنوان تخصص را برای حالت «یادگیری» تمیز می‌کند («استخدام …» را حذف می‌کند) */
function topicLabel(fa: string): string {
  let s = fa.trim()
  if (s.startsWith('استخدام ')) s = s.slice('استخدام '.length).trim()
  if (s.startsWith('دورهٔ ')) s = s.slice('دورهٔ '.length).trim()
  if (s.startsWith('دوره ')) s = s.slice('دوره '.length).trim()
  if (s.startsWith('برنامه ')) s = s.slice('برنامه '.length).trim()
  if (s.startsWith('آموزش ')) s = s.slice('آموزش '.length).trim()
  return s || fa.trim()
}

