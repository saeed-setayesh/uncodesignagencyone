import { countWords } from '@/lib/utils'
import { MIN_SERVICE_SEO_WORDS } from '@/lib/service-seo'

export type JobSeoRowInput = {
  fa: string
  en: string
  category: string
  longTail: string
  metaDescription: string
  competition: string
  searchVolume: string
  extraNote?: string
}

/**
 * متن سئو بلند (حداقل ~۸۰۰ واژه) برای جدول Job، هم‌تراز API ادمین.
 */
export function buildJobSeoBodyMarkdown(input: JobSeoRowInput): string {
  const { fa, en, category, longTail, metaDescription, competition, searchVolume, extraNote } = input

  const lines: string[] = [
    `# ${fa} — فرصت‌های شغلی و راهنمای جذب${en ? ` (${en})` : ''}\n\n`,
    `## مقدمه\n\n`,
    metaDescription
      ? `${metaDescription}\n\n`
      : `در این صفحه دربارهٔ موقعیت شغلی **${fa}** در اکوسیستم فناوری ایران، مهارت‌های رایج، مسیر رشد و نکات مهم برای کاندیداها و کارفرماها می‌خوانید.\n\n`,

    `## دسته‌بندی و زمینهٔ بازار\n\n`,
    category
      ? `نقش **${fa}** معمولاً در حوزهٔ **${category}** قرار می‌گیرد. درخواست استخدام در تیم‌های محصول، استارتاپ و شرکت‌های بزرگ بر اساس نوع پروژه و مقیاس متفاوت است.\n\n`
      : `نقش **${fa}** بسته به سازمان می‌تواند روی محصول، پشتیبانی فنی، اجرا یا رهبری تیم تمرکز داشته باشد.\n\n`,

    searchVolume
      ? `**حجم جستجوی تخمینی (کلیدواژه):** ${searchVolume}. این فقط شاخص کلی جستجوی کاربران است و نرخ تبدیل نهایی به استخدام بستگی به کیفیت آگهی، برند و شرایط قرارداد دارد.\n\n`
      : '',

    competition
      ? `**وضعیت رقابت (کلیدواژه):** ${competition}. با توجه به رقابت، محتوای دقیق، متمایز و پاسخ‌گویی به نیت جستجوی «استخدام» و «رزومه» اهمیت بیشتری دارد.\n\n`
      : '',

    longTail
      ? `## کلمات بلند (long-tail) مرتبط\n\n${longTail}\n\n`
      : `## کلمات و عبارات مرتبط با جستجو\n\n` +
        `کاندیداها اغلب ترکیب‌هایی مثل «استخدام ${fa} تهران»، «دورکاری ${fa}»، «سطح حقوق ${fa}» و «نمونه کار برای ${fa}» را جستجو می‌کنند. صفحه و آگهی شما هرچه به این نیت‌ها نزدیک‌تر باشد، اعتماد و کلیک باکیفیت‌تری جذب می‌کند.\n\n`,

    extraNote ? `## نکتهٔ استراتژیک (اولویت)\n\n${extraNote}\n\n` : '',

    `## محتوای اضافی برای کاندیداها\n\n`,
  ]

  const bodyStart = lines.join('')

  const para = (n: number) =>
    `## بخش ${n}: چرا ${fa} در بازار فناوری ایران اهمیت دارد\n\n` +
    `شرکت‌های فناوری و تیم‌های محصول برای اجرای اهداف کسب‌وکاری به نقش **${fa}** وابسته‌اند. ` +
    (category
      ? `زمینهٔ **${category}** اغلب به معنی همکاری نزدیک با تیم فنی، مدیریت محصول و گاهی تیم فروش است. `
      : '') +
    (en ? `عنوان انگلیسی رایج در آگهی‌ها: **${en}**. ` : '') +
    `اگر تیم ساختار شفاف، ابزار modern و فرهنگ یادگیری داشته باشد، نگه‌داشتن نیرو و جذب رزومهٔ بهتر ساده‌تر می‌شود. ` +
    (longTail
      ? `رصد عباراتی شبیه به «${longTail.slice(0, 120)}${longTail.length > 120 ? '…' : ''}» به تیم منابع انسانی کمک می‌کند مسیری شبیه نیت جستجوی کاربر بسازد. `
      : '') +
    `بودجه و زمان جذب باید با سطح دشواری مهارت و رقابت منطقه و صنعت هم‌خوان باشد.\n\n`

  let body = bodyStart
  for (let i = 1; i <= 18; i++) {
    body += para(i)
  }

  while (countWords(body) < MIN_SERVICE_SEO_WORDS) {
    const pad =
      `### نکات تکمیلی دربارهٔ ${fa}\n\n` +
      `اگر سازمان شما در فاز رشد است، تعریف شفاف مسئولیت، مسیر ارتقا و سطح حقوق (با توجه به ${competition || 'وضعیت رقابت'}) ` +
      `باعث می‌شود کاندیداها دقیق‌تر تصمیم بگیرند. در کنار تمرکز روی **${en || fa}**، هم‌راستا کردن توقعات تیم و مدیران با خروجی‌های قابل اندازه‌گیری (KPI) از اتلاف زمان و هزینه جلوگیری می‌کند.\n\n`
    body += pad
  }

  body +=
    `## جمع‌بندی\n\n` +
    `برای **${fa}**، تمرکز روی وضوح آگهی، تطابق با کلمات بلند (long-tail)، ` +
    `و ارائهٔ تجربهٔ داوطلب (فرآیند مصاحبه، کدریویو فنی، بستهٔ مزایا) است که هم سئو و هم تبدیل را بهبود می‌دهد. ` +
    (metaDescription ? `خلاصهٔ متا: ${metaDescription.slice(0, 200)}${metaDescription.length > 200 ? '…' : ''}\n` : '')

  return body.trim()
}
