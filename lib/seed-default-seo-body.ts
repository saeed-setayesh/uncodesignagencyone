/** متن نمونهٔ بلند برای seed (حداقل ~۸۰۰ واژه) تا ادمین بعداً ویرایش کند. */
export function buildSeedServiceSeoBody(serviceFa: string): string {
  const para = (n: number) =>
    `## بخش ${n}: چرا ${serviceFa} برای کسب‌وکار شما مهم است\n\n` +
    `در دنیای دیجیتال امروز، ${serviceFa} دیگر یک انتخاب لوکس نیست؛ بلکه بخشی از زیرساخت رقابت است. ` +
    `کسب‌وکارهایی که زودتر سرمایه‌گذاری می‌کنند، در دسترسی به مشتری، اعتماد برند و هزینهٔ جذب لید مزیت پایدار می‌گیرند. ` +
    `ما در آژانس با ترکیب استراتژی، اجرای فنی و اندازه‌گیری نتیجه، مسیر را شفاف نگه می‌داریم: از جلسهٔ اول تا تحویل و رشد ماه‌های بعد.\n\n`

  let body = `# ${serviceFa} — راهنمای جامع برای تصمیم مدیران\n\n`
  for (let i = 1; i <= 12; i++) {
    body += para(i)
    body +=
      `تجربهٔ ما در پروژه‌های مشابه نشان می‌دهد موفقیت به «جزئیات اجرا» وابسته است: سرعت سایت، ساختار سئو، پیام فروش و پشتیبانی پس از تحویل. ` +
      `اگر هر کدام ضعیف باشد، کل زنجیرهٔ بازاریابی آسیب می‌بیند. به همین دلیل قراردادها را فازبندی می‌کنیم و خروجی هر فاز را قابل اندازه‌گیری می‌کنیم.\n\n`
  }
  body +=
    `### جمع‌بندی\n\n` +
    `برای شروع، یک جلسهٔ کوتاه مشاوره کافی است تا نیاز واقعی شما در حوزهٔ ${serviceFa} مشخص شود. ` +
    `تیم ما کنار شماست تا از اتلاف بودجه در مسیرهای کم‌اثر جلوگیری شود و تمرکز روی اقدامات با بیشترین بازده باشد.\n`
  return body
}
