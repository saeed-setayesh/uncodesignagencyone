import type { PageContent } from '@/types/content'
import type { Job } from '@/lib/db'
import { SITE_URGENCY_BAR } from '@/lib/site-urgency'

type SalesBlocks = {
  h1: string
  heroSubtitle: string
  urgencyText: string
  stats: PageContent['stats']
  benefits: PageContent['benefits']
  processSteps: PageContent['processSteps']
  faq: PageContent['faq']
  ctaHeading: string
  ctaSubtext: string
}

/** عنوان نقش برای مخاطب کارفرما: «استخدام …» ابتدای عنوان ادمین حذف می‌شود تا جملهٔ معنی‌دار شود */
function clientRoleLabel(fa: string): string {
  let s = fa.trim()
  if (s.startsWith('استخدام ')) s = s.slice('استخدام '.length).trim()
  return s || fa.trim()
}

/**
 * بلوک‌های صفحهٔ «نقش/تخصص» — مخاطب اصلی کارفرمایی است که می‌خواهد این نقش را از تیم ما روی پروژهٔ خود داشته باشد، نه جویای کار.
 */
