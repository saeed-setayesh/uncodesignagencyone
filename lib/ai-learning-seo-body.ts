import { countWords } from '@/lib/utils'
import { MIN_SERVICE_SEO_WORDS } from '@/lib/service-seo'

export type AiLearningSeoInput = {
  fa: string
  en: string
  metaDescription: string
}

/** SEO body for AI tutoring courses (learn pages), not hiring pages. */
export function buildAiLearningSeoBodyMarkdown(input: AiLearningSeoInput): string {
  const { fa, en, metaDescription } = input
  const topic = fa.replace(/^(دوره|آموزش|برنامه)\s+/u, '').trim()

  const lines: string[] = [
    `# ${fa}${en ? ` (${en})` : ''}\n\n`,
    `## دربارهٔ این دوره\n\n`,
    metaDescription
      ? `${metaDescription}\n\n`
      : `دورهٔ خصوصی **${topic}** با مربی فعال در صنعت هوش مصنوعی؛ کلاس یک‌به‌یک، پروژه‌محور و متناسب با سطح شما.\n\n`,
    `## چرا این مهارت مهم است؟\n\n`,
    `هوش مصنوعی در سال‌های اخیر به یکی از پرتقاضاترین مهارت‌های بازار کار تبدیل شده است. `,
    `یادگیری **${topic}** به شما کمک می‌کند سریع‌تر وارد پروژه‌های واقعی شوید، `,
    `محصول هوشمند بسازید یا درآمد فریلنسری داشته باشید.\n\n`,
    `## مخاطبان دوره\n\n`,
    `- مبتدیان بدون پیش‌زمینهٔ فنی که می‌خواهند از صفر شروع کنند\n`,
    `- دانشجویان و فارغ‌التحصیلان که به دنبال مهارت عملی هستند\n`,
    `- کارشناسان و مدیران که می‌خواهند AI را در کار روزانه به کار ببرند\n`,
    `- فریلنسرها و بنیان‌گذارانی که می‌خواهند محصول یا سرویس AI بسازند\n\n`,
    `## سرفصل‌های نمونه\n\n`,
    `۱. مفاهیم پایه و ابزارهای اصلی\n`,
    `۲. تمرین عملی و پروژهٔ کوچک\n`,
    `۳. پیاده‌سازی سناریوی واقعی\n`,
    `۴. بهینه‌سازی، اخلاق AI و ارائهٔ خروجی\n`,
    `۵. آماده‌سازی برای بازار کار یا محصول\n\n`,
    `## فرمت آموزش\n\n`,
    `کلاس **یک‌به‌یک** با مربی، آنلاین یا حضوری، با تمرین هفتگی و بازخورد مستقیم. `,
    `سرعت و عمق مطالب با سطح و هدف شما تنظیم می‌شود — نه کلاس انبوه.\n\n`,
    `## خروجی دوره\n\n`,
    `- پروژه یا نمونه‌کار قابل ارائه\n`,
    `- مسیر شخصی‌سازی‌شده برای ادامهٔ یادگیری\n`,
    `- گواهی پایان دوره با شماره پیگیری\n`,
    `- مشاورهٔ مسیر شغلی یا محصول (در پکیج‌های جامع)\n\n`,
    `## ثبت‌نام\n\n`,
    `برای **${fa}** جلسهٔ آشنایی و تعیین سطح رایگان است. `,
    `از طریق فرم تماس یا واتساپ با ما در ارتباط باشید.\n\n`,
  ]

  let body = lines.join('')
  while (countWords(body) < MIN_SERVICE_SEO_WORDS) {
    body += `\n\n## نکات تکمیلی\n\n`
    body += `در بازار ایران، تسلط بر **${topic}** برای تیم‌های محصول، استارتاپ‌ها و سازمان‌های سنتی `,
    body += `که به دیجیتال‌سازی و اتوماسیون نیاز دارند روزبه‌روز ارزشمندتر می‌شود. `
    body += `تمرکز این دوره روی کاربرد عملی، نه فقط تئوری، است.\n\n`
  }

  return body
}
